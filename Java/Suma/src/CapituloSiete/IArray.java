public class IArray {
    public static void main(String[]args){
        int array[]={5,6,7,8,9,10,11,20,22,23,24,33,55,99};
        System.out.println("Indice\tValor");
        for(int counter=0;counter<array.length;counter++){
            System.out.println(counter+"\t"+array[counter]);
        }//end of the for
    }//End of the main
}//End of the IArray class
